export type TranslationKey = keyof typeof translations.en

export function useTranslation() {
    const { language } = useVotingContext()

    const t = (key: TranslationKey): string => {
        // Fallback to English if the string is missing in selected language
        const currentLangDict = translations[language as LanguageCode] || translations.en
        return currentLangDict[key] || translations.en[key] || key
    }

    return { t }
}
